    private void autonumber() {
        try {
            String sql = "SELECT idTransaksi FROM transaksi order by idTransaksi asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            tkode.setText("ABE001");
            while (rs.next()) {
                String id_transaksi = rs.getString("idTransaksi").substring(3);
                int AN = Integer.parseInt(id_transaksi) + 1;
                String Nol = "";
                if (AN < 10) {
                    Nol = "00";
                } else if (AN < 100) {
                    Nol = "0";
                } else if (AN < 10000) {
                    Nol = "";
                }
                tkode.setText("ABE" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto Number Gagal" + e);
        }
        tglPesan.setDateToToday();
    }

