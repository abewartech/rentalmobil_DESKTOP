    private void txtTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtTotalActionPerformed
        int Tarif = Integer.parseInt(txttarif.getText());
        int TarifSopir = 0;
        if (tarifSop.getText().length() > 0) {
            TarifSopir = Integer.parseInt(tarifSop.getText());
        }
        try {
            SimpleDateFormat myFormat = new SimpleDateFormat("yyyy-MM-dd");
            Date date1 = myFormat.parse(tglPinjam.getDateStringOrEmptyString());
            Date date2 = myFormat.parse(tglKembali.getDateStringOrEmptyString());
            long diff = date2.getTime() - date1.getTime();
            long hasildate = TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS);
            long total = (Tarif + TarifSopir) * hasildate;
            txtTotal.setText(Long.toString(total));
        } catch (ParseException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_txtTotalActionPerformed

