    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed
        String sql = "insert into kendaraan values (?,?,?,?,?,?,?,?)";
        String statusRe = null;
        if (rya.isSelected()) {
            statusRe = "Ya";
        } else if (rtidak.isSelected()) {
            statusRe = "Tidak";
        }
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, noplat.getText());
            stat.setString(2, namaKend.getText());
            stat.setString(3, tahun.getText());
            stat.setString(4, tarif.getText());
            stat.setString(5, statusRe);
            stat.setString(6, merk.getSelectedItem().toString());
            stat.setString(7, tipe.getSelectedItem().toString());
            stat.setString(8, pemilik.getSelectedItem().toString());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            noplat.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "data gagal disimpan" + e);
        }
        datatable();
    }//GEN-LAST:event_kButton1ActionPerformed

