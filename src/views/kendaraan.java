    private void datatable() {
        Object[] Baris = {"No Plat", "Nama Kendaraan", "Tahun", "Tarif", "Status", "Merek", "Tipe", "Pemilik"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtcari.getText();
        try {
            String sql = "SELECT * FROM kendaraan where noPlat like '%" + cariitem + "%' or tahun like '%" + cariitem + "%' or namaKendaraan like '%" + cariitem + "%' or namaPemilik like '%" + cariitem + "%' order by noPlat asc ";
            String sql2 = "SELECT * FROM merekKendaraan";
            String sql3 = "SELECT * FROM tipeKendaraan";
            String sql4 = "SELECT * FROM pemilik";
            Statement stat = conn.createStatement();
            Statement stat2 = conn.createStatement();
            Statement stat3 = conn.createStatement();
            Statement stat4 = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            ResultSet hasilMerk = stat2.executeQuery(sql2);
            ResultSet hasilTipe = stat3.executeQuery(sql3);
            ResultSet hasilPemilik = stat4.executeQuery(sql4);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6),
                    hasil.getString(7),
                    hasil.getString(8)
                });
            }
            merk.removeAllItems();
            while (hasilMerk.next()) {
                merk.addItem(hasilMerk.getString(2));
            }
            merk.setSelectedItem(null);
            tipe.removeAllItems();
            while (hasilTipe.next()) {
                tipe.addItem(hasilTipe.getString(2));
            }
            tipe.setSelectedItem(null);
            pemilik.removeAllItems();
            while (hasilPemilik.next()) {
                pemilik.addItem(hasilPemilik.getString(2));
            }
            pemilik.setSelectedItem(null);
            tblplgn.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil" + e);
        }
    }

